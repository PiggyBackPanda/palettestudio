// ─── Shared colour-data helper ────────────────────────────────────────────────
// All values derived from existing utils — no duplicated math.
function getColorData(hex, roles) {
  const { r, g, b } = hexToRgb(hex);
  const { h, s, l } = rgbToHsl(r, g, b);
  return {
    hex,
    r, g, b,
    h: Math.round(h),
    s: Math.round(s),
    l: Math.round(l),
    role: roles[hex] || null,
  };
}

// ─── Tailwind config generator ───────────────────────────────────────────────
function generateTailwindConfig(colors, roles, scales) {
  const roleOrder = ['Hero', 'Accent', 'Neutral', 'Background', 'Text'];
  const assignedRoles = roleOrder.filter(r => Object.values(roles).includes(r));

  const lines = [
    '// tailwind.config.js — generated by Palette Studio',
    'module.exports = {',
    '  theme: {',
    '    extend: {',
    '      colors: {',
  ];

  if (assignedRoles.length > 0) {
    lines.push('        // Core palette roles');
    assignedRoles.forEach(role => {
      const hex = Object.keys(roles).find(k => roles[k] === role);
      lines.push(`        'brand-${role.toLowerCase()}': '${hex.toLowerCase()}',`);
    });
    lines.push('');
  }

  lines.push('        // Full tint & shade scales');
  colors.forEach((hex, i) => {
    const role    = roles[hex];
    const varBase = role ? role.toLowerCase() : `color-${i + 1}`;
    const scale   = scales && scales[hex];
    if (!scale) return;
    lines.push(`        '${varBase}': {`);
    scale.forEach(s => {
      lines.push(`          ${s.step}: '${s.hex.toLowerCase()}',`);
    });
    lines.push('        },');
  });

  lines.push('      },');
  lines.push('    },');
  lines.push('  },');
  lines.push('};');

  return lines.join('\n');
}

// ─── CSS variables generator ──────────────────────────────────────────────────
function generateCSSVars(colors, roles, scales) {
  const date      = new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' });
  const roleOrder = ['Hero', 'Accent', 'Neutral', 'Background', 'Text'];

  const lines = [
    ':root {',
    '  /* Palette Studio — Brand Colours */',
    `  /* Generated: ${date} */`,
    '',
  ];

  // Core palette (role-named variables — only if assigned)
  const assignedRoles = roleOrder.filter(r => Object.values(roles).includes(r));
  if (assignedRoles.length > 0) {
    lines.push('  /* Core palette */');
    const longest = Math.max(...assignedRoles.map(r => `--color-${r.toLowerCase()}`.length));
    assignedRoles.forEach(role => {
      const hex      = Object.keys(roles).find(k => roles[k] === role);
      const varName  = `--color-${role.toLowerCase()}`;
      const pad      = ' '.repeat(Math.max(1, longest - varName.length + 1));
      const name     = getColourName(hex);
      lines.push(`  ${varName}:${pad}${hex.toLowerCase()}; /* ${name} */`);
    });
    lines.push('');
  }

  // Full palette (all colours including unassigned)
  lines.push('  /* Full palette (all colours, including unassigned) */');
  colors.forEach((hex, i) => {
    const name      = getColourName(hex);
    const roleEntry = roles[hex] ? ` /* ${name} — ${roles[hex]} */` : ` /* ${name} */`;
    lines.push(`  --color-${i + 1}: ${hex.toLowerCase()};${roleEntry}`);
  });

  // Tint & shade scales — one block per colour
  if (scales && Object.keys(scales).length > 0) {
    lines.push('');
    lines.push('  /* Tint & shade scales (OKLCH-generated, perceptually uniform) */');
    colors.forEach((hex, i) => {
      const scale   = scales[hex];
      if (!scale) return;
      const role    = roles[hex];
      const varBase = role ? `--color-${role.toLowerCase()}` : `--color-${i + 1}`;
      lines.push('');
      lines.push(`  /* ${varBase} */`);
      scale.forEach(s => {
        lines.push(`  ${varBase}-${s.step}: ${s.hex.toLowerCase()};`);
      });
    });
  }

  lines.push('}');
  return lines.join('\n');
}

