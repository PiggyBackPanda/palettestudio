// ─── Main export tab ──────────────────────────────────────────────────────────
export default function ExportTab({ colors, roles, issues, score, scales, selectedFont }) {
  // PDF state
  const [pdfLoading, setPdfLoading] = useState(false);
  const [pdfError,   setPdfError]   = useState('');

  // CSS state — regenerated whenever colors/roles/scales change
  const [copied,    setCopied]    = useState(false);
  const cssOutput = generateCSSVars(colors, roles, scales);

  // PNG state
  const [pngLoading, setPngLoading] = useState(false);
  const [pngError,   setPngError]   = useState('');

  // Tailwind state
  const [twCopied, setTwCopied] = useState(false);

  // ── Handlers ───────────────────────────────────────────────────────────────

  const handlePDFDownload = async () => {
    setPdfLoading(true);
    setPdfError('');
    try {
      const blob = await pdf(
        <BrandGuidePDF colors={colors} roles={roles} issues={issues} score={score} scales={scales} />
      ).toBlob();
      const url = URL.createObjectURL(blob);
      const a   = document.createElement('a');
      a.href     = url;
      a.download = 'palette-studio-brand-guide.pdf';
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      URL.revokeObjectURL(url);
    } catch (e) {
      setPdfError(
        'PDF generation failed. ' +
        (e.message || 'Please try again, or check the browser console for details.')
      );
    } finally {
      setPdfLoading(false);
    }
  };

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(cssOutput);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // Fallback for browsers that block clipboard without HTTPS
      try {
        const ta = document.createElement('textarea');
        ta.value = cssOutput;
        ta.style.cssText = 'position:fixed;opacity:0';
        document.body.appendChild(ta);
        ta.select();
        document.execCommand('copy');
        document.body.removeChild(ta);
        setCopied(true);
        setTimeout(() => setCopied(false), 2000);
      } catch {
        setPngError('Clipboard write failed. Please select and copy the text manually.');
      }
    }
  };

  const handlePNGDownload = async () => {
    setPngLoading(true);
    setPngError('');
    const node = buildPNGNode(colors, roles);
    document.body.appendChild(node);
    try {
      const SWATCH_W   = 120;
      const SWATCH_GAP = 20;
      const PADDING    = 20;
      const totalW     = colors.length * (SWATCH_W + SWATCH_GAP) - SWATCH_GAP + PADDING * 2;

      const canvas = await html2canvas(node, {
        scale:           2,
        backgroundColor: '#f5f2ed',
        width:           totalW,
        height:          260,
        logging:         false,
      });
      const url = canvas.toDataURL('image/png');
      const a   = document.createElement('a');
      a.href     = url;
      a.download = 'palette-studio-swatches.png';
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
    } catch (e) {
      setPngError(
        'Image generation failed. ' +
        (e.message || 'Please try again, or take a screenshot as an alternative.')
      );
    } finally {
      // Always clean up the hidden node
      document.body.removeChild(node);
      setPngLoading(false);
    }
  };


  const handleTailwindCopy = async () => {
    const twOutput = generateTailwindConfig(colors, roles, scales);
    try {
      await navigator.clipboard.writeText(twOutput);
      setTwCopied(true);
      setTimeout(() => setTwCopied(false), 2000);
    } catch {
      try {
        const ta = document.createElement('textarea');
        ta.value = twOutput;
        ta.style.cssText = 'position:fixed;opacity:0';
        document.body.appendChild(ta);
        ta.select();
        document.execCommand('copy');
        document.body.removeChild(ta);
        setTwCopied(true);
        setTimeout(() => setTwCopied(false), 2000);
      } catch { /* clipboard unavailable */ }
    }
  };
  // ── Render ─────────────────────────────────────────────────────────────────
  return (
    <div style={{ display: 'flex', flexDirection: 'column', gap: 16 }}>

      {/* ── Card 1: PDF Brand Guide ─────────────────────────────────────── */}
      <ExportCard
        title="PDF Brand Guide"
        description="A complete one-page PDF with your colour swatches, values, roles, and health score — ready to share with a designer or client."
      >
        {/* Preview of what's inside */}
        <div
          style={{
            background:          'var(--ps-bg-subtle)',
            border:              '1px solid var(--ps-border)',
            borderRadius:        'var(--ps-radius-lg)',
            padding:             '12px 14px',
            display:             'grid',
            gridTemplateColumns: 'repeat(auto-fill, minmax(160px, 1fr))',
            gap:                 6,
          }}
        >
          {[
            'Header with "Palette Studio" and date',
            `Health score bar (${score}/100)`,
            `${colors.length} colour swatch${colors.length !== 1 ? 'es' : ''} with hex, RGB, HSL`,
            'Assigned role badges',
            '60-30-10 proportion strip',
            issues.filter(i => i.type !== 'info').length > 0
              ? `${issues.filter(i => i.type !== 'info').length} issues listed`
              : 'No issues to list ✓',
          ].map((item, i) => (
            <div
              key={i}
              style={{
                fontFamily: 'var(--ps-font-ui)',
                fontSize:   'var(--ps-text-xs)',
                color:      'var(--ps-text-secondary)',
                display:    'flex',
                alignItems: 'center',
                gap:        5,
              }}
            >
              <span style={{ color: 'var(--ps-success)', fontWeight: 700 }}>✓</span>
              {item}
            </div>
          ))}
        </div>

        <ActionBtn
          onClick={handlePDFDownload}
          loading={pdfLoading}
          loadingLabel="Generating PDF…"
          error={pdfError}
        >
          ↓ Download PDF
        </ActionBtn>
      </ExportCard>

      {/* ── Card 2: CSS Variables ───────────────────────────────────────── */}
      <ExportCard
        title="CSS Variables"
        description="Copy your palette as CSS custom properties, ready to paste into any website or design system."
      >
        {/* Code preview */}
        <pre
          style={{
            background:    '#18181B',
            color:         '#e4e4e7',
            borderRadius:  'var(--ps-radius-lg)',
            padding:       '14px 16px',
            fontSize:      11,
            lineHeight:    1.6,
            overflowX:     'auto',
            overflowY:     'auto',
            maxHeight:     240,
            margin:        0,
            fontFamily:    'var(--ps-font-mono)',
            whiteSpace:    'pre',
            letterSpacing: '.02em',
          }}
        >
          {cssOutput}
        </pre>

        <button
          onClick={handleCopy}
          style={{
            background:    copied ? 'var(--ps-success)' : 'var(--ps-accent)',
            color:         'var(--ps-accent-text)',
            border:        'none',
            borderRadius:  'var(--ps-radius-md)',
            padding:       '9px 20px',
            fontFamily:    'var(--ps-font-ui)',
            fontSize:      'var(--ps-text-sm)',
            fontWeight:    500,
            cursor:        'pointer',
            letterSpacing: '.02em',
            transition:    'background .2s',
            alignSelf:     'flex-start',
          }}
          onMouseEnter={e => { if (!copied) e.currentTarget.style.background = 'var(--ps-accent-hover)'; }}
          onMouseLeave={e => { if (!copied) e.currentTarget.style.background = 'var(--ps-accent)'; }}
        >
          {copied ? '✓ Copied!' : 'Copy to Clipboard'}
        </button>
      </ExportCard>

      {/* ── Card 3: PNG Swatch Sheet ────────────────────────────────────── */}
      <ExportCard
        title="PNG Swatch Sheet"
        description="A clean image of your palette — useful for Slack, presentations, and sharing with non-designers."
      >
        {/* Live mini-preview of what the PNG will look like */}
        <div
          style={{
            background: 'var(--ps-bg-subtle)',
            borderRadius: 'var(--ps-radius-lg)',
            padding:    '14px 16px',
            display:    'flex',
            alignItems: 'flex-end',
            gap:        12,
            overflowX:  'auto',
            border:     '1px solid var(--ps-border)',
          }}
        >
          {colors.map((hex, i) => {
            const role    = roles[hex];
            const roleCol = role ? (ROLE_COL[role] || 'var(--ps-text-secondary)') : 'var(--ps-text-tertiary)';
            return (
              <div key={i} style={{ display: 'flex', flexDirection: 'column', alignItems: 'center', flexShrink: 0 }}>
                <div
                  style={{
                    width:        60,
                    height:       60,
                    borderRadius: 'var(--ps-radius-md)',
                    background:   hex,
                    border:       '1px solid rgba(0,0,0,.08)',
                    marginBottom: 5,
                  }}
                />
                <div
                  style={{
                    fontFamily:    'var(--ps-font-mono)',
                    fontSize:      8,
                    fontWeight:    500,
                    color:         'var(--ps-text-primary)',
                    letterSpacing: '.04em',
                  }}
                >
                  {hex.toUpperCase()}
                </div>
                <div
                  style={{
                    fontFamily:    'var(--ps-font-ui)',
                    fontSize:      7,
                    fontWeight:    700,
                    color:         roleCol,
                    letterSpacing: '.06em',
                  }}
                >
                  {role ? role.toUpperCase() : 'UNASSIGNED'}
                </div>
              </div>
            );
          })}
        </div>
        <div
          style={{
            fontFamily: 'var(--ps-font-ui)',
            fontSize:   'var(--ps-text-xs)',
            color:      'var(--ps-text-tertiary)',
            fontStyle:  'italic',
          }}
        >
          PNG exports at 2× resolution (retina quality).
        </div>

        <ActionBtn
          onClick={handlePNGDownload}
          loading={pngLoading}
          loadingLabel="Generating PNG…"
          error={pngError}
        >
          ↓ Download PNG
        </ActionBtn>
      </ExportCard>

      {/* ── Card 4: Tailwind Config ─────────────────────────────────────── */}
      <ExportCard
        title="Tailwind CSS Config"
        description="Add your palette to tailwind.config.js. Includes the full tint & shade scale for each colour."
      >
        <pre
          style={{
            background:    '#18181B',
            color:         '#e4e4e7',
            borderRadius:  'var(--ps-radius-lg)',
            padding:       '14px 16px',
            fontSize:      11,
            lineHeight:    1.6,
            overflowX:     'auto',
            overflowY:     'auto',
            maxHeight:     240,
            margin:        0,
            fontFamily:    'var(--ps-font-mono)',
            whiteSpace:    'pre',
            letterSpacing: '.02em',
          }}
        >
          {generateTailwindConfig(colors, roles, scales)}
        </pre>

        <button
          onClick={handleTailwindCopy}
          style={{
            background:    twCopied ? 'var(--ps-success)' : 'var(--ps-accent)',
            color:         'var(--ps-accent-text)',
            border:        'none',
            borderRadius:  'var(--ps-radius-md)',
            padding:       '9px 20px',
            fontFamily:    'var(--ps-font-ui)',
            fontSize:      'var(--ps-text-sm)',
            fontWeight:    500,
            cursor:        'pointer',
            letterSpacing: '.02em',
            transition:    'background .2s',
            alignSelf:     'flex-start',
          }}
          onMouseEnter={e => { if (!twCopied) e.currentTarget.style.background = 'var(--ps-accent-hover)'; }}
          onMouseLeave={e => { if (!twCopied) e.currentTarget.style.background = 'var(--ps-accent)'; }}
        >
          {twCopied ? '✓ Copied!' : 'Copy to Clipboard'}
        </button>
      </ExportCard>

      {/* ── Design Tokens section ──────────────────────────────────────── */}
      <div style={{ marginTop: 8 }}>
        <ExportTokensTab colors={colors} roles={roles} selectedFont={selectedFont} />
      </div>

      {/* ── Platform-specific export guides ────────────────────────────── */}
      <div style={{ marginTop: 8 }}>
        <PlatformExportSection colors={colors} roles={roles} />
      </div>

    </div>
  );
}
